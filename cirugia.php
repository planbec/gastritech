<h3>Programas de tratamiento del c&aacute;ncer</h3>
<p>Los principales objetivos de un programa de diagn&oacute;stico y tratamiento del c&aacute;ncer son curar o prolongar considerablemente la vida de los pacientes, y garantizar la mejor calidad de vida posible a quienes sobreviven a la enfermedad.</p>
<p>Los programas de tratamiento m&aacute;s eficaces y eficientes son los que:</p>
	<ul>
		<li>Funcionan de forma continuada y equitativa;</li>
		<li>Est&aacute;n vinculados a sistemas de detecci&oacute;n precoz;</li>
		<li>Respetan normas de atenci&oacute;n basadas en datos probatorios y aplican un enfoque multidisciplinario.</li>
	</ul>

<p>Adem&aacute;s, esos programas garantizan un tratamiento adecuado para los tipos de c&aacute;ncer que, si bien no se prestan a una detecci&oacute;n precoz, tienen un alto potencial de curaci&oacute;n o buenas probabilidades de que la supervivencia se prolongue con una buena calidad de vida (como el c&aacute;ncer de mama y los linfomas en fase avanzada).</p>

<h3>Diagn&oacute;stico</h3>
<p>La primera fase fundamental en el tratamiento del c&aacute;ncer es establecer un diagn&oacute;stico basado en un examen patol&oacute;gico. Para determinar la extensi&oacute;n del tumor hay que realizar una serie de pruebas. La estatificaci&oacute;n suele requerir recursos cuantiosos que pueden resultar prohibitivos en entornos de escasos recursos, donde, debido a un diagn&oacute;stico tard&iacute;o, que a su vez es consecuencia del acceso deficiente a la atenci&oacute;n, en la mayor parte de los pacientes la enfermedad se encuentra ya en fase avanzada.</p>
<p>Una vez establecidos en la medida de lo posible el diagn&oacute;stico y el grado de extensi&oacute;n del tumor, hay que tomar una decisi&oacute;n sobre el tratamiento m&aacute;s eficaz en un determinado entorno socioecon&oacute;mico.</p>

<h3>Principales modalidades de tratamiento</h3>
<p>Para ello hay que seleccionar cuidadosamente una o varias modalidades de tratamiento principales - cirug&iacute;a, radioterapia y tratamiento sistémico -, elecci&oacute;n que debe basarse en pruebas cient&iacute;ficas sobre el mejor tratamiento existente teniendo presentes los recursos disponibles.</p>
<p>Cuando el tumor est&aacute; localizado y es de peque&ntilde;o tama&ntilde;o, es probable que la cirug&iacute;a, y a veces la radioterapia, alcancen muy buenos resultados por s&iacute; solas. La quimioterapia por s&iacute; sola puede ser eficaz para un peque&ntilde;o número de tipos de c&aacute;ncer, como las neoplasias hematol&oacute;gicas (leucemias y linfomas), que por lo general puede considerarse que est&aacute;n extendidas desde el principio.</p>
<p>Una modalidad de tratamiento combinado requiere la estrecha colaboraci&oacute;n de todo el equipo de atenci&oacute;n oncol&oacute;gica.</p>

